package kr.co.steamsale.backend.data;

public class FranchiseChild {
	int id;
	String name;
	// 0 - App , 1 - Sub
	int type;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
}
